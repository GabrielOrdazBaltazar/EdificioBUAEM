package Clases;
    
public class Institucion {
    
    //Atributos
    private String nombre;
    
    //Metodos 
    public Institucion() {
        
    }

    public Institucion(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
